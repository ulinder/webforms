**Hjärt-/Kärlsjukdom
*Frågor om symtom på kärlkramp:
Ett tryck mitt över bröstet, ofta vid ansträngning?
Andfåddhet som kommer i samband med ansträngning? 
En känsla av att det är trångt i bröstet?
Smärta i bröstet som ibland strålar ut i en arm, mot halsen, ryggen eller underkäke?
Att en sådan känsla kan komma när du anstränger dig fysiskt eller vid starka känsloyttringar eller upprördhet?
Att du vaknat nattetid av ovanstående känslor?
Att du upplevt sådana känslor i kall väderlek, blåst och/eller stress?
*Frågor om symtom på förmaksflimmer:
Oregelbunden och snabb puls emellanåt?
Känner du igen ett eller flera av dessa symptom: Försämrad fysisk ork, yrsel, svårt att andas, trötthet och svaghet, ont i bröstet, svimning?
*Frågor om symtom på hjärtsvikt:
Trötthet och mindre ork?
Lätt blir andfådd?
Att du har svullna anklar och ben?
Har hjärtklappning?
Har svårt att sova plant?
Ofta behöver du gå upp och kissa på natten?
Fråga till dig som är en 65 årig man: Har aortascreening gjorts?

**Andningsvägar (KOL)
*Vid KOL är det vanligt vid flera av följande symptom.
Upplever du emellanåt att:
Du blir andfådd när du anstränger dig?
Känner du dig hindrad i rörelser i vardagen (t.ex. bära kassar/gå upp för backar)?
Du känner du dig trött och orkar mindre?
Du behöver allt oftare hosta upp slem?
Du har väsande, rosslande eller pipande andning – särskilt vid ansträngning?
Du får ofta infektioner i luftvägarna? Du går ner i vikt och får svullna fötter?

**Tandvård
Har du träffat någon tandläkare eller tandhygienist de senaste två åren?
Har du besvär från munnen? - T.ex. värk, svullnad, trasiga tänder eller fyllningar, lösa tänder, illasittande proteser, muntorrhet, sår eller blåsor?
Har du dålig andedräkt eller dålig smak i munnen?
Är det något du undviker att äta för att det är besvärligt at tugga eller svälja?
Påverkas din mathållning av exempelvis muntorrhet, illasittande proteser, tandförluster eller smärta i munnen?

**Riskfaktorer för Cancer
Har du en nytillkommen trötthet, som du inte känner igen sedan tidigare? 
Har du fått nytillkommen smärta eller värk?
Har du fått en större viktnedgång ofrivilligt?
Har du känt någon knöl någonstans på kroppen?
Har du ett födelsemärke som blivit större, ändrat färg eller blött? 
Gör det ont/känns konstigt/går trögt när du äter?
Har du blivit hes/fått hosta i mer än 3 veckor?
Har du sett blod i din urin?
Har du sett blod, rött eller svart i din avföring?
Har du fått förändrade avföringsvanor som diarré, förstoppning eller att din tarmtömning ändrat sig på något sätt?

*OM kvinna
Har du känt någon knöl/förhårdnad i bröstet?
Har du varit på mammografi? 
Har du varit och tagit gynekologiskt cellprov?
